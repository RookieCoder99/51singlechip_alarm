void judge_leap_month(uchar year,uchar month,uchar day)
{
	if((sun_to_lunar(0,year,month,day,2)==sun_to_lunar(0,year,month-1,day,2))
		&& sun_to_lunar(0,year,month,day,1)==sun_to_lunar(0,year,month-1,day,1))
	{
		leap_month_flag=1;
	}
	else if((sun_to_lunar(0,year,month,day,2)==sun_to_lunar(0,year,month-1,day+1,2))
		&& sun_to_lunar(0,year,month,day,1)==sun_to_lunar(0,year,month-1,day+1,1))
	{
		leap_month_flag=1;
	}
	else if((sun_to_lunar(0,year,month,day,2)==sun_to_lunar(0,year,month-1,day+2,2))
		&& sun_to_lunar(0,year,month,day,1)==sun_to_lunar(0,year,month-1,day+2,1))
	{
		leap_month_flag=1;
	}
	else if((sun_to_lunar(0,year,month,day,2)==sun_to_lunar(0,year,month-1,day-2,2))
		&& sun_to_lunar(0,year,month,day,1)==sun_to_lunar(0,year,month-1,day-2,1))
	{
		leap_month_flag=1;
	}	
	else if((sun_to_lunar(0,year,month,day,2)==sun_to_lunar(0,year,month-1,day-1,2))
		&& sun_to_lunar(0,year,month,day,1)==sun_to_lunar(0,year,month-1,day-1,1))
	{
		leap_month_flag=1;
	}	
	else
	{
		leap_month_flag=0;
	}
}
