void hb_show_temp(uchar int_tmp, uchar dec_tmp)
{
	uchar* tmp ;
	uchar symbol = 0;
	uint dec_temper = 0;
	if((int_tmp &0x80) != 0)
	{
		symbol = 1;				//符号为负
		int_tmp = ~int_tmp;
		//dec_temper = (0x0F-dec_temper) +1;	//负数取反加1
		dec_tmp=dec_tmp&0x0f;
		dec_tmp= (0x0F-dec_tmp) +1;
		//dec_tmp=~dec_tmp+0x01;
		if(dec_tmp == 0x10)			//小数部分进位，则整数进位
		{
			int_tmp += 1;
			dec_tmp = 0;
		}
	}
	if(symbol==1)
	{
		Display_StrChar(STRCHAR_12_12,0x04,0x0e,"-");//显示温度符号位
		Delay_1ms(DELAY_TIME);
	}
	else
	{
		Display_StrChar(STRCHAR_12_12,0x04,0x0e," ");//显示温度符号位
		Delay_1ms(DELAY_TIME);
	}
	tmp=Int_to_Str(int_tmp);//显示温度整数部分
	Display_StrChar(STRCHAR_12_12,0x04,0x0f,tmp);
	Delay_1ms(DELAY_TIME);
	
	Display_StrChar(STRCHAR_12_12,0x04,0x11,".");//显示小数点
	Delay_1ms(DELAY_TIME);

	dec_temper = (uint)dec_tmp;
	dec_temper = dec_temper * 625 + 500;	//对百分位进行四舍五入
	dec_temper = dec_temper/100;		//只显示两位小数
	tmp=Int_to_Str(dec_temper);
	tmp[1]='\0';
	Display_StrChar(STRCHAR_12_12,0x04,0x12,tmp);
	Display_StrChar(STRCHAR_12_12,0x04,0x13,"C");
}


