void send(uchar ch) 
{	
	while(BUSY);						//等待海比邻液晶空闲
	P0=ch;							//数据从P1口送出
	REQ=0;							//请求液晶读取数据
	nop_();nop_();nop_();nop_();					//适当延时
	nop_();nop_();nop_();nop_();
	nop_();nop_();nop_();nop_();
	REQ=1;							//结束本次数据传送
}


